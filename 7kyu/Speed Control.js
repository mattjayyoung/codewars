function gps(s, x) {
    let avgHourlySpeed = []

    if (x.length <= 1) return 0

    for (let i = 0; i < x.length - 1; i++){
        let deltaDistance = x[i + 1] - x[i]
        avgHourlySpeed.push((3600 * deltaDistance) / s) 
    }

    return Math.floor(Math.max(...avgHourlySpeed))
}



