// Take the First N Elements - 8kyu (December 3rd)

function take(arr, n) {
  let results = []

  n = n <= arr.length ? n : arr.length
  

  if (arr.length > 0){
    for (let i = 0; i < n; i++){
      results.push(arr[i])
    }
    return results
  }

  return results
}

